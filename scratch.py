x = np.array([[-0.99974874,  0.01690088 ,-0.01472488  ,0.13339397],
                [-0.01717819, -0.99967353 , 0.01891422, -0.00105937],
                [-0.0144004  , 0.01916241 , 0.99971267,  0.0118687 ],
                [ 0.  ,        0.     ,     0.    ,      1.        ]])


def InverseT(x):
    R = x[:3,:3]
    p = x[:,3][:3]
    RT = R.T
    ip = np.matmul(-1 * RT, p)
    y = np.array([[R.T[0,0], R.T[0,1], R.T[0,2], ip[0]],
                  [R.T[1,0], R.T[1,1], R.T[1,2], ip[1]],
                  [R.T[2,0], R.T[2,1], R.T[2,2], ip[2]],
                 [0,0,0,1]])
    return y
